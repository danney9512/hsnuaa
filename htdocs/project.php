					<h3>急難救助專案</h3>
					<div class="image fit">
						<img src="images/project/project_E.jpg" alt="" />
					</div>
					<p>提案人：H465孟繁宏、H542李如玉 &nbsp&nbsp&nbsp&nbsp 專案召集：H465孟繁宏
					<br>緣起於2016年考進母校科學班之基隆武崙國中畢業生，因家庭經濟困難，一度考慮放棄入學，經校友轉知和秘書處前往了解狀況後，於眾多校友的支持下，正式向校友會理監事會提出急難救助專案，以便正式進行募款及後續支援母校學弟妹。由母校及校友提供需要協助對象，本會針對個案成立支援小組，組員五名(含召集人)，負責訪問了解學弟妹的狀況，評估並提出支援方案，由校友會決議通過後補助之。補助分為常態性支援以及突發事件支援：
					<br>常態性支援：學弟妹需要長期穩定的支援，由支援小組評估補助支援方式，並鼓勵學弟妹能先採取學生工讀方式，不足之處由校友會每月按時補助，並透過導師了解學弟妹的各項狀況，以了解是否需要調整支援內容。接受常態性支援的學弟妹，每學期透過導師了解其學習狀況，並要求其學期成績應保持在全班前50%，並沒有小過以上的紀錄。
					<br>突發事件支援：家中突遭變故，在短期內亟需支援幫助，因為是緊急事件，所以由支援小組立即評估決議，可在理監事上追認，為一次性支援，上限為一萬元整。
					<br>本會每年公布專案補助收支金額明細，以及受助人概況。但不公布受助人班級姓名以保護學弟妹隱私，專案募款目標金額：每年40萬元。
					<br>原提案時間：2016.07.10</p>
